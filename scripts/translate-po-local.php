        $current_msgid .= $m[1];
        $output_lines[] = $line;
    }
    // Start of msgstr
    elseif ( preg_match( '/^msgstr "(.*)"\s*$/', $trimmed, $m ) ) {
        $in_msgid = false;
        $in_msgstr = true;
        $existing = $m[1];
        
        // If empty and we have a translation
        if ( $existing === '' && ! empty( $current_msgid ) ) {
            $total_empty++;
            
            if ( isset( $dictionary[ $current_msgid ] ) ) {
                $translation = $dictionary[ $current_msgid ];
                $translation = str_replace( '"', '\\"', $translation );
                $output_lines[] = 'msgstr "' . $translation . '"';
                $translated_count++;
            } else {
                $output_lines[] = $line;
            }
        } else {
            $output_lines[] = $line;
        }
        
        $current_msgid = '';
    }
    // Continuation of msgstr
    elseif ( $in_msgstr && preg_match( '/^"(.*)"\s*$/', $trimmed, $m ) ) {
        $output_lines[] = $line;
    }
    // Other lines
    else {
        $in_msgid = false;
        $in_msgstr = false;
        $output_lines[] = $line;
    }
}

